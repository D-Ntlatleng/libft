#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long i;

	i = (long)n;
	if (i < 0)
	{
		ft_putchar_fd('-', fd);
		i *= -1;
	}
	if (i > 10)
		ft_putnbr_fd(i / 10, fd);
	ft_putchar_fd((i % 10) + 48, fd);
}
